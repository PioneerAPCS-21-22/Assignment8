public class Main {
    public static void main(String[] args) {
        double a = 10;
        int b = 5;
        double diff = sub(a, b);
        System.out.println(a + " - " + b + " = " + diff);
    }

    public static double sub(double m, int n) {
        double diff = m - n;
        return diff;
    }
}


